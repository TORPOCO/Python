# Programa: Multiplicacion de cadenas

# 1. Crear un separador visual
linea = "=" * 20
print(linea)
# Salida: ==========

# 2. Indentación simple
nivel = 2
sangria = "   " * nivel
print(sangria + "definicion_bloque_codigo:")
# Salida:        definicion_bloque_codigo:

# 3. Patrones simples
print("1" * 10) # "111111" (Texo, no número)

#Multiplicacion con tuplas
resultado = 5*('Hola',10)
print(f'Resultado: {resultado}')

#Multiplicacion con listas
resultado = 10*[0]
print(f'Resultado: {resultado}')