# Profundizando Sistemas Numeración

# Decimal
a= 10
print(f'a: {a}')

# Binario
a = 0b1010
print(f'a: {a}')

# Octal
a = 0o12
print(f'a: {a}')

# Hexadecimal
a= 0xA
print(f'a: {a}')

# Convertir un tipo entero, incluyendo base
#     (Basee, Decimal)
a = int('23', 10)
print(f'a: {a}')


# Base Binario
a = int('10111', 2)
print(f'a: {a}')

# Base  Octal
a = int('27', 8)
print(f'a: {a}')

# Base Hexadecimal
a = int('344', 16)
print(f'a: {a}')