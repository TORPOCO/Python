# Tenemos una cadena que representa un número
numero_cadena = "25"
# Convertimos la cadena a entero
numero_entero = int(numero_cadena)
# Convertimos el entero a flotante
numero_decimal = float(numero_entero)
# Convertimos el número a cadena nuevamente
numero_texto = str(numero_decimal)
# Mostramos los resultados
print("Entero:", numero_entero)
print("Decimal:", numero_decimal)
print("Texto:", numero_texto)